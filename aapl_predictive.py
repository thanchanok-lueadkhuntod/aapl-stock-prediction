import pandas as pd
import numpy as np
import yfinance as yf
import matplotlib.pyplot as plt
from sklearn.linear_model import LinearRegression
from sklearn.metrics import mean_squared_error, r2_score, accuracy_score, recall_score, classification_report

"""Phase 1: Data Overview"""

df = pd.read_csv('aapl_us_2025.csv')

df['Date'] = pd.to_datetime(df['Date'])

df.set_index('Date', inplace=True)

df.sort_index(inplace=True)

df = df[['Close']].copy()

print(f"Total rows: {df.shape[0]}")
print(df.head())

"""Phase 2: Feature Engineering"""

df['MA30'] = df['Close'].rolling(window=30).mean()

df['Daily_Return'] = df['Close'].pct_change() * 100

df['Next_Close'] = df['Close'].shift(-1)

df.dropna(inplace=True)

plt.figure(figsize=(8, 6))
plt.scatter(df['MA30'], df['Close'], alpha=0.5, color='royalblue', s=10)
corr_value = df['Close'].corr(df['MA30'])
plt.title(f'Scatter Plot: Close Price vs MA30 (Correlation: {corr_value:.4f})')
plt.xlabel('MA30 (USD)')
plt.ylabel('Close Price (USD)')
plt.grid(True)
plt.show()

plt.figure(figsize=(12, 6))
plt.plot(df.index[-500:], df['Close'].iloc[-500:], label='Close Price', color='black', alpha=0.7)
plt.plot(df.index[-500:], df['MA30'].iloc[-500:], label='MA30', color='orange', linewidth=2)
plt.title('Price Trend vs MA30 (Last 500 Days)')
plt.xlabel('Date')
plt.ylabel('Price (USD)')
plt.legend()
plt.grid(True)
plt.show()

plt.figure(figsize=(10, 6))
plt.hist(df['Daily_Return'], bins=100, color='purple', alpha=0.7, edgecolor='black')
plt.axvline(x=0, color='red', linestyle='--', label='0% Return')
plt.title('Daily Return Distribution')
plt.xlabel('Daily Return (%)')
plt.ylabel('Frequency (Days)')
plt.legend()
plt.grid(True, alpha=0.3)
plt.show()

"""Phase 3: Exploratory Data Analysis"""

corr_value = df['Close'].corr(df['MA30'])

"""Phase 4: Machine Learning Modeling"""

split_idx = int(len(df) * 0.8)

train_df = df.iloc[:split_idx].copy()
test_df = df.iloc[split_idx:].copy()

features = ['Close', 'MA30', 'Daily_Return']
target = 'Next_Close'

X_train = train_df[features]
y_train = train_df[target]

X_test = test_df[features]
y_test = test_df[target]

model = LinearRegression()
model.fit(X_train, y_train)

predictions = model.predict(X_test)

rmse = np.sqrt(mean_squared_error(y_test, predictions))
r2 = r2_score(y_test, predictions)

print("Statistical Evaluation")
print(f"R-Squared (R2): {r2:.4f}")
print(f"RMSE: {rmse:.2f} USD")

"""Phase 5: Business Logic & Derived Classsification"""

results = test_df.copy()
results['Predicted_Next_Close'] = predictions

results['Signal'] = np.where(results['Predicted_Next_Close'] > results['Close'], 1, 0)

results['Actual_Direction'] = np.where(results['Next_Close'] > results['Close'], 1, 0)

accuracy = accuracy_score(results['Actual_Direction'], results['Signal'])
recall = recall_score(results['Actual_Direction'], results['Signal'])

print("Business Logic Evaluation")
print(f"Accuracy (Win Rate): {accuracy * 100:.2f}%")
print(f"Recall (Catching the Uptrend): {recall * 100:.2f}%")
print("\nClassification Report:\n", classification_report(results['Actual_Direction'], results['Signal'], zero_division=0))

plt.figure(figsize=(14, 6))
plt.plot(results.index[-100:], results['Next_Close'].iloc[-100:], label='Actual Next Close', color='black')
plt.plot(results.index[-100:], results['Predicted_Next_Close'].iloc[-100:], label='Predicted Next Close', color='red', linestyle='dashed')
plt.title('AAPL Stock Price Prediction (Last 100 Days)')
plt.xlabel('Date')
plt.ylabel('Price (USD)')
plt.legend()
plt.grid(True)
plt.show()

"""Phase 6: Backtesting and Alpha Calculation"""

start_date = results.index[0].strftime('%Y-%m-%d')
end_date = results.index[-1].strftime('%Y-%m-%d')
market = yf.download("^GSPC", start=start_date, end=end_date)

my_money = 10000.0
money_history = []

for i in range(len(results) - 1):
    today = results.index[i]
    tomorrow = results.index[i+1]

    signal = results.loc[today, 'Signal']

    if signal == 1:
        # ถ้าถือหุ้นอยู่ เงินเราจะเพิ่มขึ้นหรือลดลง ตามราคาหุ้นของวันพรุ่งนี้
        price_today = results.loc[today, 'Close']
        price_tomorrow = results.loc[tomorrow, 'Close']
        
        # หาเปอร์เซ็นต์ที่หุ้นขึ้นหรือลง
        percent_change = (price_tomorrow - price_today) / price_today
        
        # อัปเดตยอดเงินในพอร์ต
        my_money = my_money + (my_money * percent_change)
        
    money_history.append(my_money)

money_history.append(my_money)
results['Portfolio_Value'] = money_history

# คำนวณผลตอบแทน
strategy_profit_percent = ((my_money - 10000.0) / 10000.0) * 100

market_start = market['Close'].iloc[0].item()
market_end = market['Close'].iloc[-1].item()
market_profit_percent = ((market_end - market_start) / market_start) * 100

days_tested = (results.index[-1] - results.index[0]).days
years_tested = days_tested / 365.25

annual_strategy_return = strategy_profit_percent / years_tested
annual_market_return = market_profit_percent / years_tested

alpha = annual_strategy_return - annual_market_return

print(f"ผลตอบแทนของกลยุทธ์ต่อปี (Strategy): {annual_strategy_return:.2f}%")
print(f"ผลตอบแทนของตลาดต่อปี (S&P 500): {annual_market_return:.2f}%")
print(f"Alpha: {alpha:.2f}%")
